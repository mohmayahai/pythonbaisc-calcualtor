operator = input('Enter an operator( + , -, * ,/): ')
num1 = int(input('Enter the 1st number'))
num2 = int(input('enter the 2nd number'))


if operator=='+':
    print(num1+num2)
elif operator=='-':
    print(num1-num2)
elif operator == '*':
    print(num1*num2)
else:
    print(num1/+num2)



